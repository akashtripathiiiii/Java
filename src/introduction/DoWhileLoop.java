package introduction;

public class DoWhileLoop {
    public static void main(String[] args){
        //do{
           //statement
        //}while(condition)
        System.out.println("DoWhile Loop");
        int i = 0; // initialisation
        do {
            System.out.println(i);
            i++; // updation
        } while(i<=20);
    }
}
